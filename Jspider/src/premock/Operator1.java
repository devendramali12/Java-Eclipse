package premock;

public class Operator1 {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println(a + b);

		int i1 = 5;
		char ch = '5';
		System.out.println(i1 + ch);

		byte b1 = 5;
		int i2 = 5;
		System.out.println(b1 + i2);

		int i3 = 5;
		double d1 = 10;
		System.out.println(i3 + d1);

		int i4 = 5;
		String s1 = "5";
		System.out.println(i4 + s1);
	}

}
